#include "libft.h"

int	ft_is_in_str(char c, char *s)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != 0)
		{
			if (s[i] == c)
				return (1);
			else
				i++;
		}
	}
	return (0);
}
